// ============================================================
// Module: TokenStatSRAM_197x16
//
// Purpose:
//   Store runtime inv_rms[t] for 197 tokens.
//   Write side is driven by:
//      RMS_Stat_Accumulator -> AddrGen -> InvSqrt LUT
//   Read side is used by:
//      Streaming_RMSNorm_Unit.inv_rms_addr / inv_rms_data
//
// Note:
//   This version uses combinational read so that inv_rms_data is
//   aligned with Streaming_RMSNorm_Unit's current token address.
//   If you change this to synchronous BRAM, add a 1-cycle alignment
//   pipeline around x_in / gamma_data / inv_rms_data.
// ============================================================

module TokenStatSRAM_197x16 #(
    parameter int TOKEN_NUM = 197,
    parameter int TOKEN_AW  = 8,
    parameter int DATA_W    = 16
)(
    input  logic clk,
    input  logic rst_n,

    input  logic                 wr_valid_i,
    input  logic [TOKEN_AW-1:0]   wr_addr_i,
    input  logic [DATA_W-1:0]     wr_data_i,

    input  logic [TOKEN_AW-1:0]   rd_addr_i,
    output logic [DATA_W-1:0]     rd_data_o
);

    logic [DATA_W-1:0] mem [0:TOKEN_NUM-1];

    integer i;

    always_ff @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            for (i = 0; i < TOKEN_NUM; i = i + 1) begin
                mem[i] <= '0;
            end
        end
        else begin
            if (wr_valid_i && (wr_addr_i < TOKEN_NUM[TOKEN_AW-1:0])) begin
                mem[wr_addr_i] <= wr_data_i;
            end
        end
    end

    always_comb begin
        if (rd_addr_i < TOKEN_NUM[TOKEN_AW-1:0])
            rd_data_o = mem[rd_addr_i];
        else
            rd_data_o = '0;
    end

endmodule
