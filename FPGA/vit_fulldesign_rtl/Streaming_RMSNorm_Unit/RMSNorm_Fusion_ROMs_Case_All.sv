// ============================================================
// Module: RMSInvSqrtAddrGen_A_Global
//
// Purpose:
//   Convert raw RMS sum_sq into the 10-bit address for
//   RMSInvSqrtLUT_A_Global_Case.
//
// From software params:
//   mean_sq = sum_sq / 384
//   idx = clamp(round(mean_sq * 3.6303390809), 0, 1023)
//
// Combined:
//   idx = clamp(round(sum_sq * (3.6303390809 / 384)), 0, 1023)
//
// This module uses Q16 fixed-point coefficient:
//   INDEX_SCALE_OVER_CHANNEL_Q = round((3.6303390809 / 384) * 2^16)
//                              = 620
// ============================================================

module RMSInvSqrtAddrGen_A_Global #(
    parameter int SUM_W  = 32,
    parameter int ADDR_W = 10,
    parameter int SCALE_Q = 16,
    parameter int INDEX_SCALE_OVER_CHANNEL_Q = 620
)(
    input  logic [SUM_W-1:0]  sum_sq_i,
    output logic [ADDR_W-1:0] lut_addr_o
);

    logic [63:0] prod;
    logic [63:0] idx_round;

    always_comb begin
        prod      = {{(64-SUM_W){1'b0}}, sum_sq_i} * INDEX_SCALE_OVER_CHANNEL_Q;
        idx_round = (prod + (64'd1 << (SCALE_Q-1))) >> SCALE_Q;

        if (idx_round > 64'd1023)
            lut_addr_o = {ADDR_W{1'b1}};
        else
            lut_addr_o = idx_round[ADDR_W-1:0];
    end

endmodule


